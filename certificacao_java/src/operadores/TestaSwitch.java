package operadores;

public class TestaSwitch {

	public static void main(String[] args) {
		int opcao = 4;

		switch (opcao) {
		case 1:
			System.out.println(" PRIMEIRA OPCAO");
			break;
		case 2:
			System.out.println(" SEGUNDA OPCAO");
			break;
		case 3:
			System.out.println(" TERCEIRA OPCAO");
			break;
		default:
			System.out.println(" DEFAULT ");
		}
	}

}
