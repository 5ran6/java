package fundamentos;

public class Inferencia {
	
	public static void main(String[] args) {
		double a = 4.5;
		System.out.println(a);
		
		var b = 77.7;
		System.out.println(b);
		
		var c = "Texto";
		System.out.println(c);

		c = "Outro Texto";
		System.out.println(c);
		
		double d;
		d = 126.36;
		System.out.println(d);
	}

}
